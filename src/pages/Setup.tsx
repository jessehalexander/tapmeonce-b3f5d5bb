// ─── N8N AI Bio webhook ───────────────────────
const N8N_WEBHOOK = 'https://airtribe.app.n8n.cloud/webhook/tapmeonce-profile';

// ─── Main Component ───────────────────────────
export default function Setup() {
  const navigate = useNavigate();
  const [searchParams] = useSearchParams();
  const [state, setState] = useState<SetupState>({
    ...INITIAL_STATE,
    plan: (searchParams.get('plan') as PlanId) || 'free',
  });
  const [showExitDialog, setShowExitDialog] = useState(false);
  const [pendingStep, setPendingStep] = useState<number | null>(null);
  const [usernameStatus, setUsernameStatus] = useState<'idle' | 'checking' | 'taken' | 'available'>('idle');
  const [showPassword, setShowPassword] = useState(false);
  const [generatingBio, setGeneratingBio] = useState(false);
  const [avatarPreview, setAvatarPreview] = useState<string | null>(null);
  const [isSubmitting, setIsSubmitting] = useState(false);
  const [billingCycle, setBillingCycle] = useState<'monthly' | 'yearly'>('monthly');
  const [triedNext, setTriedNext] = useState(false);
  const usernameTimer = useRef<ReturnType<typeof setTimeout>>();
  const fileInputRef = useRef<HTMLInputElement>(null);
  // Tracks whether username is still auto-generated (true) or manually edited (false)
  const usernameAutoGenerated = useRef(true);

  const update = useCallback(<K extends keyof SetupState>(key: K, val: SetupState[K]) => {
    setState(prev => ({ ...prev, [key]: val }));
  }, []);

  // ─── Username check ───────────────────────
  useEffect(() => {
    if (!state.username || state.username.length < 3) {
      setUsernameStatus('idle');
      return;
    }
    setUsernameStatus('checking');
    clearTimeout(usernameTimer.current);
    usernameTimer.current = setTimeout(async () => {
      const available = await checkUsernameAvailable(state.username);
      setUsernameStatus(available ? 'available' : 'taken');
    }, 600);
    return () => clearTimeout(usernameTimer.current);
  }, [state.username]);

  // ─── Auto-suggest username from name ──────
  // Only runs while username hasn't been manually edited
  useEffect(() => {
    if (!usernameAutoGenerated.current) return;
    const suggestion = state.fullName
      .toLowerCase()
      .replace(/[^a-z0-9]/g, '')
      .slice(0, 20);
    update('username', suggestion); // Also clears when fullName is cleared
  }, [state.fullName]);

  // ─── Pre-fill WhatsApp from phone when entering step 4 ──
  useEffect(() => {
    if (state.step === 4 && !state.whatsappNumber && state.phone) {
      update('whatsappNumber', `${state.countryCode}${state.phone.replace(/\D/g, '')}`);
    }
  }, [state.step]);

  // ─── Navigation guards ────────────────────
  const goToStep = (next: number) => {
    // If going back from payment step, show exit confirmation
    if (state.step === 5 && next < 5) {
      setPendingStep(next);
      setShowExitDialog(true);
      return;
    }
    update('step', next);
    window.scrollTo({ top: 0, behavior: 'smooth' });
  };

  const canProceed = (): boolean => {
    switch (state.step) {
      case 1: return !!state.plan;
      case 2:
        return !!(
          state.fullName.trim() &&
          state.username.length >= 3 &&
          usernameStatus === 'available' &&
          state.email.includes('@') &&
          state.password.length >= 8 &&
          state.phone.replace(/\D/g, '').length >= 10
        );
      case 3: return !!(state.designation.trim());
      case 4: return true; // Links are optional
      case 5: return true;
      default: return false;
    }
  };

  // ─── AI Bio generation ────────────────────
  const buildFallbackBio = () => {
    const role = state.designation || 'professional';
    const org = state.company ? ` at ${state.company}` : '';
    const loc = state.location ? `, ${state.location}` : '';
    if (state.isStudent) {
      return `I'm a ${role}${org}${loc} — curious, driven, and always building. I love turning ideas into real things and connecting with people who think differently. Open to internships, collaborations, and anything that pushes me to grow.`;
    }
    return `I'm a ${role}${org}${loc}. I bring energy, clarity, and a results-first mindset to everything I do. Whether it's strategy, execution, or people — I show up with intention. Let's connect.`;
  };

  const generateBio = async (tone: 'formal' | 'friendly' | 'bold' = 'formal') => {
    if (!state.designation && !state.company) {
      toast.error('Add your designation first so AI can craft the perfect bio');
      return;
    }
    setGeneratingBio(true);
    try {
      const controller = new AbortController();
      const timeout = setTimeout(() => controller.abort(), 30000); // 30s timeout — OpenAI can be slow
      const res = await fetch(N8N_WEBHOOK, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          full_name: state.fullName,
          job_title: state.designation,
          company: state.company || '',
          bio_raw: state.bio || '',
          tone: tone,                                          // "formal" | "friendly" | "bold"
          is_student: state.isStudent,
          plan: state.plan,
          location: state.location || '',
        }),
        signal: controller.signal,
      });
      clearTimeout(timeout);
      if (!res.ok) throw new Error('non-ok');
      const data = await res.json();
      const bio = data.ai_bio || data.bio || data.result || data.output || data.text || '';
      if (!bio) throw new Error('empty-response');
      update('bio', bio);
      // Also use tagline as a hint if available
      if (data.ai_tagline && !state.designation) update('designation', data.ai_tagline);
      toast.success('AI bio generated! Feel free to edit it.');
    } catch (err: any) {
      // CORS / network / timeout → use a smart template as fallback
      const fallback = buildFallbackBio();
      update('bio', fallback);
      const isCors = err?.name === 'TypeError' || err?.message === 'Failed to fetch';
      if (isCors) {
        toast.info('AI service has a CORS issue — generated a starter bio instead. You can edit it freely, or fix CORS in your n8n webhook to enable full AI.');
      } else {
        toast.info('AI service unavailable — generated a starter bio instead. Feel free to edit it.');
      }
    } finally {
      setGeneratingBio(false);
    }
  };

  // ─── Avatar upload ────────────────────────
  const handleAvatarChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    const file = e.target.files?.[0];
    if (!file) return;
    update('avatarFile', file);
    const url = URL.createObjectURL(file);
    setAvatarPreview(url);
  };

  // ─── Links management ─────────────────────
  const addLink = (platform: string, placeholder = '') => {
    const exists = state.links.find(l => l.platform === platform);
    if (exists) return;
    update('links', [
      ...state.links,
      { platform: platform as SocialPlatform, label: platform, url: '', sort_order: state.links.length, mode: 'both', is_active: true },
    ]);
  };

  const updateLink = (idx: number, field: string, value: string) => {
    const updated = [...state.links];
    updated[idx] = { ...updated[idx], [field]: value };
    update('links', updated);
  };

  const removeLink = (idx: number) => {
    update('links', state.links.filter((_, i) => i !== idx));
  };

  // ─── Final submit ─────────────────────────
  // NOTE: Razorpay bypassed for now — account created directly.
  // Payment integration will be wired up in a future release.
  const handleSubmit = async () => {
    await createAccount();
  };

  const createAccount = async (paymentId?: string) => {
    if (!isSupabaseConfigured) {
      toast.error('⚙️ Backend not connected yet. Add your Supabase URL and key in Lovable → Project Settings → Environment Variables, then redeploy.');
      return;
    }
    setIsSubmitting(true);
    try {
      // 1. Create Supabase auth user
      const { data: authData, error: authError } = await signUp(state.email, state.password, {
        full_name: state.fullName,
      });
      if (authError) throw authError;
      const userId = authData.user?.id;
      if (!userId) throw new Error('Account creation failed');

      // 2. Upload avatar if provided
      let avatarUrl: string | null = null;
      if (state.avatarFile) {
        avatarUrl = await uploadAvatar(userId, state.avatarFile);
      }

      // 3. Create profile
      await supabase.from('profiles').insert({
        user_id: userId,
        username: state.username,
        full_name: state.fullName,
        email: state.email,
        phone: state.phone,
        country_code: state.countryCode,
        avatar_url: avatarUrl,
        designation: state.designation,
        company: state.isStudent ? null : state.company,
        institution: state.isStudent ? state.company : null,
        is_student: state.isStudent,
        bio: state.bio,
        location: state.location,
        plan: state.plan,
        active_mode: 'business',
        is_active: true,
        lead_gen_consent: state.leadGenConsent,
      });

      // 4. Save links
      if (state.links.length > 0) {
        const linksToInsert = state.links
          .filter(l => l.url?.trim())
          .map((l, i) => ({
            user_id: userId,
            platform: l.platform,
            label: l.label || l.platform,
            url: l.url!,
            sort_order: i,
            mode: 'both',
            is_active: true,
          }));
        if (linksToInsert.length) {
          await supabase.from('links').insert(linksToInsert);
        }
      }

      // 5. Add WhatsApp link if a number was provided
      const waRaw = (state.whatsappNumber || '').replace(/\D/g, '');
      if (waRaw.length >= 10) {
        const waUrl = `https://wa.me/${waRaw}`;
        await supabase.from('links').insert({
          user_id: userId,
          platform: 'whatsapp',
          label: 'WhatsApp',
          url: waUrl,
          sort_order: 0,
          mode: 'both',
          is_active: true,
        });
      }

      // 6. Create order record if paid
      if (state.plan !== 'free') {
        const totals = calculateOrderTotal(state.plan, state.cardType, billingCycle);
        await supabase.from('orders').insert({
          user_id: userId,
          plan: state.plan,
          card_type: state.cardType,
          card_price: totals.cardPrice,
          subscription_price: totals.subscriptionPrice,
          total_amount: totals.total,
          razorpay_payment_id: paymentId,
          status: 'placed',
          shipping_address: state.shippingAddress,
        });
      }

      // 7. Create placeholder NFC card record
      await supabase.from('nfc_cards').insert({
        user_id: userId,
        card_type: state.cardType,
        status: 'pending',
      });

      // 8. Apply referral if provided
      if (state.referralCode) {
        await supabase.from('referral_uses').insert({
          code: state.referralCode,
          used_by: userId,
        });
      }

      toast.success('Account created! Welcome to TapMeOnce 🎉');
      navigate('/dashboard');
    } catch (err: any) {
      toast.error(err.message || 'Something went wrong. Please try again.');
    } finally {
      setIsSubmitting(false);
    }
  };

  const initiateRazorpayPayment = async () => {
    if (!isSupabaseConfigured) {
      toast.error('⚙️ Backend not connected yet. Add your Supabase URL and key in Lovable → Project Settings → Environment Variables, then redeploy.');
      return;
    }
    if (!import.meta.env.VITE_RAZORPAY_KEY_ID) {
      toast.error('⚙️ Razorpay not connected yet. Add your VITE_RAZORPAY_KEY_ID in Lovable → Project Settings → Environment Variables, then redeploy.');
      return;
    }
    setIsSubmitting(true);
    const totals = calculateOrderTotal(state.plan, state.cardType, billingCycle);
    try {
      // Create Razorpay order via your backend/Supabase Edge Function
      const { data, error } = await supabase.functions.invoke('create-razorpay-order', {
        body: {
          amount: totals.total * 100, // paise
          plan: state.plan,
          billing_cycle: billingCycle,
          email: state.email,
          name: state.fullName,
        },
      });
      if (error) throw error;

      await initiatePayment({
        orderId: data.order_id,
        amount: totals.total * 100,
        currency: 'INR',
        name: state.fullName,
        email: state.email,
        phone: `${state.countryCode}${state.phone}`,
        plan: state.plan,
        description: `TapMeOnce ${PLANS[state.plan].name} Plan`,
        onSuccess: async (paymentId) => {
          await createAccount(paymentId);
        },
        onFailure: (err) => {
          toast.error('Payment failed. Please try again.');
          setIsSubmitting(false);
        },
      });
    } catch (err: any) {
      toast.error('Could not initiate payment. Please try again.');
      setIsSubmitting(false);
    }
  };

  const orderTotals = calculateOrderTotal(state.plan, state.cardType, billingCycle);
  const hasNoLinks = state.links.filter(l => l.url?.trim()).length === 0;

  return (
    <div className="min-h-screen bg-background flex flex-col">
      {/* ─── Top bar ─── */}
      <header className="sticky top-0 z-50 border-b border-border bg-background/80 backdrop-blur">
        <div className="container h-16 flex items-center justify-between">
          <a href="/"><img src={logo} alt="TapMeOnce" className="h-12" /></a>
          <StepIndicator current={state.step} total={STEPS.length} labels={STEPS} />
          <div className="w-24 text-right text-xs text-muted-foreground">
            Step {state.step} of {STEPS.length}
          </div>
        </div>
      </header>

      {/* ─── Content ─── */}
      <main className="flex-1 container max-w-2xl py-8 px-4 pb-28">
        <AnimatePresence mode="wait">
          <motion.div
            key={state.step}
            initial={{ opacity: 0, x: 24 }}
            animate={{ opacity: 1, x: 0 }}
            exit={{ opacity: 0, x: -24 }}
            transition={{ duration: 0.25 }}
          >
            {state.step === 1 && <StepPlan state={state} update={update} billingCycle={billingCycle} setBillingCycle={setBillingCycle} />}
            {state.step === 2 && <StepAccount state={state} update={update} usernameStatus={usernameStatus} showPassword={showPassword} setShowPassword={setShowPassword} triedNext={triedNext} />}
            {state.step === 3 && <StepProfile state={state} update={update} avatarPreview={avatarPreview} fileInputRef={fileInputRef} handleAvatarChange={handleAvatarChange} generatingBio={generatingBio} generateBio={generateBio} />}
            {state.step === 4 && <StepLinks state={state} update={update} addLink={addLink} updateLink={updateLink} removeLink={removeLink} />}
            {state.step === 5 && <StepCheckout state={state} update={update} orderTotals={orderTotals} billingCycle={billingCycle} setBillingCycle={setBillingCycle} />}
          </motion.div>
        </AnimatePresence>

        {/* No-links warning */}
        {state.step === 4 && hasNoLinks && (
          <motion.div
            initial={{ opacity: 0, y: 8 }}
            animate={{ opacity: 1, y: 0 }}
            className="mt-4 p-3 rounded-lg bg-amber-500/10 border border-amber-500/30 flex gap-3 items-start text-sm"
          >
            <Info className="h-4 w-4 text-amber-400 mt-0.5 shrink-0" />
            <span className="text-amber-300">
              <strong>Heads up:</strong> According to LinkedIn research, profiles with at least one social link
              get <strong>10x more engagement</strong> than those without. Add a link to boost your impact!
            </span>
          </motion.div>
        )}
      </main>

      {/* ─── Sticky bottom navigation — always visible on mobile ─── */}
      <div className="sticky bottom-0 z-40 bg-background/95 backdrop-blur-sm border-t border-border">
        <div className="container max-w-2xl flex items-center justify-between py-4 px-4">
          {state.step > 1 ? (
            <Button variant="ghost" onClick={() => goToStep(state.step - 1)} className="gap-2">
              <ChevronLeft className="h-4 w-4" /> Back
            </Button>
          ) : (
            <Button variant="ghost" onClick={() => navigate('/')} className="gap-2">
              <ChevronLeft className="h-4 w-4" /> Home
            </Button>
          )}

          {state.step < STEPS.length ? (
            <Button
              onClick={() => {
                if (!canProceed()) {
                  setTriedNext(true);
                  if (state.step === 2) {
                    if (!state.fullName.trim()) toast.error('Please enter your full name');
                    else if (state.username.length < 3) toast.error('Username must be at least 3 characters');
                    else if (usernameStatus === 'taken') toast.error('That username is taken — try a different one');
                    else if (usernameStatus === 'checking') toast.error('Checking username availability, please wait…');
                    else if (!state.email.includes('@')) toast.error('Please enter a valid email address');
                    else if (state.password.length < 8) toast.error('Password must be at least 8 characters');
                    else if (state.phone.replace(/\D/g, '').length < 10) toast.error('Please enter a valid 10-digit phone number');
                  }
                  return;
                }
                setTriedNext(false);
                goToStep(state.step + 1);
              }}
              className="bg-gradient-gold text-primary-foreground hover:opacity-90 gap-2"
            >
              Continue <ChevronRight className="h-4 w-4" />
            </Button>
          ) : (
            <Button
              onClick={handleSubmit}
              disabled={isSubmitting || !state.shippingAddress.name}
              className="bg-gradient-gold text-primary-foreground hover:opacity-90 gap-2 min-w-32"
            >
              {isSubmitting ? (
                <><Loader2 className="h-4 w-4 animate-spin" /> Creating Account…</>
              ) : (
                <>Create Account <Check className="h-4 w-4" /></>
              )}
            </Button>
          )}
        </div>
      </div>

      {/* ─── Exit confirmation ─── */}
      <AlertDialog open={showExitDialog} onOpenChange={setShowExitDialog}>
        <AlertDialogContent>
          <AlertDialogHeader>
            <AlertDialogTitle>Exit payment?</AlertDialogTitle>
            <AlertDialogDescription>
              Your payment hasn't been completed yet. Your plan details will be saved but you'll need to come back to finish.
            </AlertDialogDescription>
          </AlertDialogHeader>
          <AlertDialogFooter>
            <AlertDialogCancel onClick={() => setShowExitDialog(false)}>Stay</AlertDialogCancel>
            <AlertDialogAction onClick={() => {
              setShowExitDialog(false);
              if (pendingStep !== null) update('step', pendingStep);
            }}>Yes, go back</AlertDialogAction>
          </AlertDialogFooter>
        </AlertDialogContent>
      </AlertDialog>
    </div>
  );
}


  username: '',
  email: '',
  password: '',
  phone: '',
  countryCode: '+91',
  isStudent: false,
  company: '',
  avatarFile: undefined,
  designation: '',
  bio: '',
  location: '',
  links: [],
  whatsappSameAsPhone: true,
